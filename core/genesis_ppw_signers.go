func getMainNetPpwSignStr() string {

	var buf bytes.Buffer
	buf.WriteString("0x")

	for _, str := range ppwMainNetSigAddr {
		addr := strings.ToLower(str[:])
		if strings.HasPrefix(addr,"0x") {
			buf.WriteString(addr[2:])
		} else {
			buf.WriteString(addr[:])
		}
	}

	//fmt.Print(buf.String())
	return buf.String()

}


